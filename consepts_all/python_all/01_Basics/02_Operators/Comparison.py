'''
| Operator | Meaning          | Example          |
| -------- | ---------------- | ---------------- |
| `==`     | Equal            | `5 == 5 → True`  |
| `!=`     | Not equal        | `5 != 3 → True`  |
| `>`      | Greater than     | `5 > 3 → True`   |
| `<`      | Less than        | `5 < 3 → False`  |
| `>=`     | Greater or equal | `5 >= 5 → True`  |
| `<=`     | Less or equal    | `5 <= 3 → False` |
'''
x = 5
y = 3

print(x == y)  # False
print(x != y)  # True
print(x > y)   # True

