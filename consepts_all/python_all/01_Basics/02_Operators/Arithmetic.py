'''
| Operator | Meaning        | Example       |
| -------- | -------------- | ------------- |
| `+`      | Addition       | `5 + 3 = 8`   |
| `-`      | Subtraction    | `5 - 3 = 2`   |
| `*`      | Multiplication | `5 * 3 = 15`  |
| `/`      | Division       | `5 / 2 = 2.5` |
| `%`      | Modulus        | `5 % 2 = 1`   |
| `**`     | Exponentiation | `5 ** 2 = 25` |
| `//`     | Floor Division | `5 // 2 = 2`  |
'''
x = 10
y = 3

print(x + y)  # 13
print(x - y)  # 7
print(x * y)  # 30
print(x / y)  # 3.333...
print(x % y)  # 1
print(x ** y)  # 1000
print(x // y)  # 3
